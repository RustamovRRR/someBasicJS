// using for loop

for (let i = 0; i < 10; i++) {
  console.log(`number is ${i}`);
}

console.log("------------- decrease -------------");

for (let i = 10; i > 0; i--) {
  console.log(`number is ${i}`);
}

console.log("----------- adding 2 ----------------");

for (let i = 0; i < 10; i += 2) {
  console.log(`number is ${i}`);
}
